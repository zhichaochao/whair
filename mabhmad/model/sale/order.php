		$query = $this->db->query("SELECT DISTINCT email FROM `" . DB_PREFIX . "order` o LEFT JOIN " . DB_PREFIX . "order_product op ON (o.order_id = op.order_id) WHERE (" . implode(" OR ", $implode) . ") AND o.order_status_id <> '0'");

		return $query->row['email'];
	}


	/**
	 * 查询订单数据,将数据导出到excel中
	 * @author   dyl   783973660@qq.com  2016.9.30
	 * @param    Array   $data   筛选条件数组
	 */
    public function getExportOrderData($data = array()) {

		$sql = "SELECT o.order_id, o.order_no, CONCAT(o.firstname, ' ', o.lastname) AS customer,
                o.email, o.telephone, o.payment_address_1 as payment_address, o.shipping_address_1 as shipping_address,
                o.shipping_city as city, o.shipping_zone as state, o.shipping_postcode as zip_code,
                o.shipping_country as country, o.payment_method, o.shipping_method, o.payment_type,
                (SELECT ot.value FROM " . DB_PREFIX . "order_total ot WHERE ot.order_id = o.order_id AND ot.code = 'shipping') AS ship_fee,
                (SELECT GROUP_CONCAT(op.model) FROM " . DB_PREFIX . "order_product op WHERE op.order_id = o.order_id) AS sku,
                (SELECT sum(op.quantity) FROM " . DB_PREFIX . "order_product op WHERE op.order_id = o.order_id) AS total_qty,
                o.total, o.currency_code, o.date_added,
                (SELECT os.name FROM " . DB_PREFIX . "order_status os WHERE os.order_status_id = o.order_status_id
                AND os.language_id = " . (int)$this->config->get('config_language_id') . ") AS status
                FROM " . DB_PREFIX . "order o";

		if (isset($data['filter_order_status'])) {
			$implode = array();

			$order_statuses = explode(',', $data['filter_order_status']);

			foreach ($order_statuses as $order_status_id) {
				$implode[] = "o.order_status_id = " . (int)$order_status_id;
			}

			if ($implode) {
				$sql .= " WHERE (" . implode(" OR ", $implode) . ")";
			} else {

			}
		} else {
			$sql .= " WHERE o.order_status_id > 0";
		}

		if (!empty($data['filter_order_id'])) {
			$sql .= " AND o.order_id = " . (int)$data['filter_order_id'];
		}

		if (!empty($data['filter_order_no'])) {
			$sql .= " AND o.order_no like '%" . $data['filter_order_no'] . "%'";
		}

		if (!empty($data['filter_customer'])) {
			$sql .= " AND CONCAT(o.firstname, ' ', o.lastname) LIKE '%" . $this->db->escape($data['filter_customer']) . "%'";
		}

		//添加的开始时间
		if (!empty($data['filter_date_added'])) {
			$sql .= " AND DATE(o.date_added) >= DATE('" . $this->db->escape($data['filter_date_added']) . "')";
		}
        //修改的开始时间
		if (!empty($data['filter_date_modified'])) {
			$sql .= " AND DATE(o.date_modified) >= DATE('" . $this->db->escape($data['filter_date_modified']) . "')";
		}
        //添加的结束时间
		if (!empty($data['filter_date_added_endtime'])) {
			$sql .= " AND DATE(date_added) <= DATE('" . $this->db->escape($data['filter_date_added_endtime']) . "')";
		}
        //修改的结束时间
		if (!empty($data['filter_date_modified_endtime'])) {
			$sql .= " AND DATE(date_modified) <= DATE('" . $this->db->escape($data['filter_date_modified_endtime']) . "')";
		}
		//订单的支付类型
		if(!empty($data['filter_payment_type'])){
			$sql .= " AND payment_type = '" .$data['filter_payment_type']. "'";
		}

		if (!empty($data['filter_total'])) {
			$sql .= " AND o.total = " . (float)$data['filter_total'];
		}

		$sql .=" ORDER BY o.date_added DESC, o.date_modified DESC ";

		if (isset($data['start']) || isset($data['limit'])) {
			if ($data['start'] < 0) {
				$data['start'] = 0;
			}

			if ($data['limit'] < 1) {
				$data['limit'] = 20;
			}

			$sql .= " LIMIT " . (int)$data['start'] . "," . (int)$data['limit'];
		}

		$query = $this->db->query($sql);

		return $query->rows;
	}

